// Generates src/rules/common/diacritics.ts — a complete, engine-independent
// diacritic-folding table that reproduces V8's
//   s.normalize('NFD').replace(/[̀-ͯ]/g, '')
// for every precomposed accented letter across ALL scripts (Latin, Greek,
// Cyrillic, Vietnamese, …). QuickJS/Javy has no String.prototype.normalize, so we
// bake the data here from Node's (V8) NFD so the JS and WASM builds fold accents
// identically. Hangul/other canonical decompositions that produce no combining
// mark are intentionally omitted: they never change a diacritic-insensitive
// comparison (both operands decompose the same way).
// Regenerate with: node scripts/gen-diacritics.mjs
import { writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const COMBINING = /[̀-ͯ]/;
const STRIP = /[̀-ͯ]/g;
const entries = [];
for (let cp = 0xc0; cp <= 0xffff; cp++) {
  if (cp >= 0xd800 && cp <= 0xdfff) continue; // surrogates
  const ch = String.fromCodePoint(cp);
  const nfd = ch.normalize('NFD');
  if (nfd === ch || !COMBINING.test(nfd)) continue; // only real diacritics
  const folded = nfd.replace(STRIP, '');
  if (folded && folded !== ch) entries.push([ch, folded]);
}

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const obj = '{\n' + entries.map(([k, v]) =>
  `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`).join('\n') + '\n}';
const ts = `// AUTO-GENERATED by scripts/gen-diacritics.mjs — do not edit by hand.
// ${entries.length} precomposed accented characters → their base form, matching
// V8's normalize('NFD') + combining-mark strip, for every script. Engine-
// independent so the QuickJS/WASM build folds accents identically to the JS build.

export const DIACRITIC_FOLD: Record<string, string> = ${obj};
`;
writeFileSync(join(root, 'src/rules/common/diacritics.ts'), ts);
console.log('wrote src/rules/common/diacritics.ts with', entries.length, 'entries');
